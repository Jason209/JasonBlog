				<h1 class="blog-tiitle">Google+ 社交再尝试</h1>
				<aside class="blog-aside">
				<span>Google / </span> 
				<time datetime="12:31  2012.3.22">12:31  2012-03-22</time>
				</aside>
				<section cite="" class="blog-detail">
					<div class="introduction"><strong>[核心提示]</strong> Google 推出了 Google+，对社交网络进行了又一次尝试。本文对 Google+ 的背景，特色，功能，优劣势等进行了分析，并对 Google+ 与其竞争对手进行了比较。 </div>
					<p>在过去的几年中，社交网络领域似乎已经尘埃落定，Twitter、Facebook、Youtube各自占领了其稳定的位置。在这样的情况下，还有别的社交网络能够在这一领域站稳脚跟吗？除了一些非常垂直的主题社区（如Instagram）外，成功者鲜有耳闻。但经历了Orkut、Friend Connect、Buzz、Wave等失败的Google显然迫切需要在社交获得成功，上周推出的Google＋就是其又一次尝试</p>
					<img class="illustration" src="../images/article-1-1.png" />
					<h2>迫切进入社交领域的Google</h2>
					<p>Google之所以那么迫切的想要进入社交领域，与其核心业务是密切相关的。</p>
					<p>说到底，Google是一家搜索公司，其所有业务都是为了搜索服务。将索引全世界的信息作为己任的Google在这之前一直认为：在信息检索方面，机器比人更为精确、可靠。这一想法曾经属实，Google正是凭借其高明的自动化算法获得了现在的成功。但社交网络的出现打破了这一平衡，Twitter、Facebook的迅速崛起让Google看到了社交网络的巨大潜力，也看到了现实的用户在信息筛选方面的独特优势。毕竟，搜索的最终目的是为了人们能够更方便的获取信息，相对传统的基于链接的PageRank算法，基于社交网络的信息权重更能反映人们在现实中的真实需求。而这，对于致力于挖掘出互联网上最准确、最相关信息的Google来说，是一个至关重要的参数。</p>
					<p>从根本上讲，Google真正需要的并非是一个自己掌控的社交网络，而是社交网络上人们所生成的各种信息和信号。只要能够获取其他社交网络的这些信息，Google完全能够借此完善自己的搜索帝国。Facebook显然不会向Google提供信息，与Twitter合作推出的社交搜索、实时搜索具有很大潜力，但Google明显仍然没有找到最好的将搜索与社交结合起来的方法。也许只有自己亲自尝试做出一个社交网络，才能够更好的理解人们的社交信号对搜索的影响。</p>
					<p>而Google在社交领域的尝试也从来没有停止过。Orkut仅仅在巴西、印度取得了成功，而在其他地方反应平平；Friend Connect随着Facebook的大行其道，渐渐淡出人们的视线；Buzz因为其最初推出时所带来的巨大隐私问题的冲击，慢慢被人们所抛弃；Wave由于模糊的宣传定位被冤枉的当作社交网络产品，而其真正的通讯协作功能则因为性能不佳而饱受非议，最终被Google移交给Apache基金会，成为一个开源的协作平台。</p>
					<img class="illustration" src=
					"../images/article-1-2.png" />
					<h2>Google＋从前辈学到了什么</h2>
					<p>在有其他强大的对手的时候，推出一个孤立的社交产品对用户来说是没有任何吸引力的。“网络效应”在社交网络领域发挥了巨大的作用，这正是众多号称Facebook杀手的社交网络难以对Facebook造成威胁的原因。</p>
					<p>Google过去在社交领域的失败很大程度上源于其缺乏一个整体的社交策略。而与之前Orkut、Buzz等产品不同，这次Google显然是打出了自己的王牌，Google＋将不仅仅是一个独立的社交产品，而是贯穿Google整个生态系统的变革计划。这也许就是Google＋这一名称的含义：依然是Google，但会是更好的Google，进化后的Google。</p>
					<h2>Google＋有何不同？</h2>
					<img class="illustration" src=
					"../images/article-1-3.png" />
					<p>从界面上来看，目前的Google＋仿佛是一个更为简洁、美观的Facebook。但一旦开始使用，你就能立即发现他们的不同。</p>
					<h3>Circles & Streams</h3>
					<img class="illustration" src=
					"../images/article-1-4.png" />
					<p>乍看上去，Circles好像是Facebook Groups的美化版。但实际上，Circles是Google＋的核心组件。Circles的概念源自于现实生活中人们不同的社交圈：有些人属于我们的朋友圈，有些属于我们的同事圈，或是家人、熟人、某个兴趣圈子等，而不同圈子之前也可能有一定的重合。在Circles里，我们可以像现实生活中那样，将好友添加进不同的圈子进行分组，一个好友也可以同时属于多个圈子。</p>
					<h2>Google+的优势</h2>
					<p>无论是Twitter与Google的实时搜索合作的中断，还是Facebook匆忙宣布的Skype整合，都表明Facebook、Twitter对这次Google＋的发布相当重视。这是Google之前推出各种社交产品时没有看见过的场景。也许Google这次终于抓住了机会。</p>
					<p>与Facebook、Twitter相比，Google庞大的业务范围是其拥有的一个最大的优势。从搜索、邮件、在线协作、地图、视频、浏览器、手机，Google拥有无数最优秀的产品，也拥有超过10亿的用户。Google的意图明显是将Google＋渗透到自己的所有业务中去，正如目前正在进行中的Google界面的大改良。只要Google能够将自己的众多产品与Google＋实现无缝的整合，吸引自己其他服务的用户进入Google＋的世界，那么Google提供的这一社交平台无疑将是Facebook、Twitter无法匹敌的。</p>
					<h2>Google+的缺陷</h2>
					<p>当然，Google+的缺陷也还不少。例如缺乏像Facebook Credit那样的支付手段，Stream排序方式还不够优化，缺乏公司/企业页面，未开放第三方API，尚未具有明确的平台计划，以及用户数量上的巨大差距。但Google＋毕竟是一个仅仅推出一周多的产品，是否能够吸引更多的用户是Google在下一步的战略中重要的一步。</p>
					<p>此外，Google+目前与Google其他服务的整合也还非常原始，仅靠一个导航栏的状态提醒极大的限制了Google+的功用。Google+仍然感觉像一个孤立的产品，我们需要的是Google+与Gmail、Google Docs、Google Reader以及其他Google服务更深度的整合。例如将Google Reader里的分享和评论带进Google+（这点在Buzz里其实曾经存在过，也许Google还在考虑如何更好的进行整合？），又如将Circles引入Gmail、Google Docs、Google Calendar，这样我们就可以方便的给某一群联系人群发邮件、共享文档日程、协同办公等。Android客户端里的群聊功能可以直接向指定的Circles发送信息是一个很好的开始。</p>
					<h2>Google+的竞争对手们</h2>
					<img class="illustration" src=
					"../images/article-1-5.png" />
					<p>值得注意的是，Facebook与当前Google+的面对用户群有显著差别。Facebook更多面对的是普通用户，更强调亲属、好友间的交流，而Google+在当前测试阶段，面向的都是比较熟悉互联网的高级用户，强调的是共同兴趣人群的分享和讨论。两者有着非常不同的社交图谱。而从这个角度来看，Twitter与Google+的面向人群有非常显著的重合。在现在这个阶段，我们观察到Google+上讨论的活跃程度远远高于Twitter上，也许Twitter应该更加小心Google+的挑战。</p>
					<h2>总结</h2>
					<p>相比之前的产品，Google+无疑拥有一个完美的开局。Google是否能够延续Google+的热度，吸引更多的用户和开发者，就让我们拭目以待。在这里，我先给Google+一个+1。</p>
